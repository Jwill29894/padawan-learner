package papu.code.foo;

public class ComparisonOperators {
    public static void main(String[] args){
        int AlanAge = 20;
        int JonaAge = 17;
        System.out.println(AlanAge < JonaAge);
        System.out.println(AlanAge <= JonaAge);
        System.out.println(AlanAge > JonaAge);
        System.out.println(AlanAge >= JonaAge);
        System.out.println(AlanAge == JonaAge);
        System.out.println(AlanAge != JonaAge);
        System.out.println(AlanAge < JonaAge);
    }
}